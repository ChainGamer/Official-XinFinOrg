                    <p>
                        <b>XDC is positioned to connect real world finance origination to DeFi markets</b>. We want to set <b>XDC as a de-facto standard for recording payment obligations</b> from institutions which includes
                        <b>buyers, suppliers, governments and banks</b>. Payment obligations can also be recorded for employer-employee which can potentially open up the payday loans, mortgages, auto loans on XDC Network.
                        <b>Governments raise billions through bonds</b> which are essentially payment obligations. Currently this space is empty and there is no platform which focuses on this particular feature. Enabling this brings a
                        massive opportunity to bring <b>DeFi backed by real world assets on top of XDC network</b>. <b>Central banks pour liquidity in financial markets</b> to make liquidity reach at <b>grassroots level at MSMEs</b>. XDC is
