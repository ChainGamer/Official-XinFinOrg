                            <div>
                                <p>The XDC network hosts four different kinds of nodes. Many nodes don't require the functionality of or have the resources to run full nodes. Nodes that only write to the private state of a sub-network don't require all components of the XDC protocol. Furthermore, the ability to run full nodes will also be restricted to institutions that meet the criteria for it.</p>
                            </div>
