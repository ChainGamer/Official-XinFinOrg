                            <div>
                                <p>Online wallets often seem to be a convenient way of storing cryptocurrency tokens but could actually be very insecure. Certain implementations of online wallets are simply web applications with access to wallets running on a full node. It is extremely insecure to share private and public keys, something that is inevitable with most online wallet implementations. An added concern is the accidental or planned shutting down of the node hosting the wallet. In that case all tokens are lost or stolen. These incidents are not only financially damaging but also hurt the general reputation of the Blockchain ecosystem. 
The XDC ecosystem will provide easily accessible light wallets that will connect securely to full nodes. Each light wallet will have unique keys that are used to sign transactions from their associated account. Since the light wallet is a standalone application that serves only one account, the possibility of being hacked or losing the XDC tokens becomes highly unlikely.</p>
