                            <h3>● What is Hybrid blockchain?</h3>
                            <div>
                                <p>XinFin has developed a highly scalable, secure, permissioned and commercial grade Blockchain architecture that combines the best features of Bitcoin, Quorum and Ethereum. It's a combination of permissioned and public chain that addresses scalability and security. The Proof of Authority eventually leads to Proof of Stake. XinFin blockchain is powered by XDC01 protocol, underlying XDC tokens can be used for real time payment and settlement.</p>
                                <p>The XDC Blockchain is built upon the paradigm of consortium Blockchains. The architecture differs from conventional private/permissioned Blockchains as well as public Blockchains. Built upon the Ethereum codebase, the XDC Blockchain also deals with the system state rather than blocks of transactions.</p>
                                <p>There are two different kinds of networks that can exist within the XDC ecosystem. Firstly, the public network that all constituents are part of and a private/permissioned network that restricts participation. And secondly, the private network state is maintained in its respective network but a record (hash) of transactions and smart contracts is stored on the public state of the Blockchain.</p>
                                <p>The public state of the XDC Blockchain is shared by all participating nodes that are owned by different kinds of constituents. Groups of nodes can further form fully permissioned networks with their own private state that is accessible only to authorized members. For instance, let us assume that a private marketplace for goods and services is set up in Network 1. The specifics of the trade between parties are not accessible to Network 2. But, the record of individual trades is stored as hashes on the public state that is shared by all such that even in the private network there is an immutable record of transactions.</p>
                            </div>
                        </li>
                        <li>
                            <h3>● Is Hybrid Blockchain based on POW or POS?</h3>
                            <div>
                                <p>The XDC hybrid blockchain does not have Proof of Work. Our consensus architecture is similar to Proof of Authority. Groups of trusted but distributed nodes take part in the consensus mechanism. There are stake requirements for these nodes to participate in the process. These stake requirements guarantee the correctness and agreement of the network as a whole.</p>
                            </div>
                        </li>
                        <li>
                            <h3>● How many transactions are you looking at optimally? Horizontal side chains?</h3>
                            <div>
                                <p>Since, the core consensus process of the XDC protocol is architecturally similar to Proof of Authority, while deriving the benefits of proof of stake; it does not use the expensive and energy heavy proof of work. This not just ensures that the network is a "green network" but is also able to process anywhere from 150 to 300 transactions per second. Our in-house R&D team is working on throughput improvements in both the public and private networks of the XDC blockchain.</p>
                            </div>
                        </li>
                        <li>
                            <h3>● How can institutions participate?</h3>
                            <div>
                                <p>Hybrid architecture of the XDC protocol allows institutions to build their projects over the permissioned sub-network connected to XDC protocol. The institutions can choose to work with a fully permissioned instance allowing full security of sensitive financial data, work with XDC protocol as a messaging layer or connect to the various digital asset liquidity mediums through the XDC protocol.</p>
                            </div>
                        </li>
                        <li>
                            <h3>● What is the consensus architecture for XDC Blockchain?</h3>
                            <div>
                                <p>The consensus architecture for the XDC protocol is divided into two parts. The first is the XDC consortium membership requirements. In order to participate in the XDC network, institutions must belong to one of the three tiers. The tiers, in part, correspond to the XDC token holdings. Any fraudulent activity results in seizure of the XDC tokens that are staked to gain membership and host network infrastructure.</p>
                                <p>The second is the actual process of achieving consensus. Once the network topology is stable, nodes come together to vote on transactions that can be put on a new block. Then a new leader is selected from among the validator nodes at random. This leader creates a new block with the transactions that have been voted on by the other full nodes.</p>
                            </div>
                        </li>
